X = 15
Y = 10

print("--- Operadores Relacionais em Python ---")
print(f"Valores: X = {X}, Y = {Y}\n")

# Igual a (==)
print(f"X == Y (15 == 10)? Resultado: {X == Y}")

# Diferente de (!=)
print(f"X != Y (15 != 10)? Resultado: {X != Y}")

# Maior que (>)
print(f"X > Y (15 > 10)? Resultado: {X > Y}")

# Menor que (<)
print(f"X < Y (15 < 10)? Resultado: {X < Y}")

# Maior ou igual a (>=)
print(f"X >= Y (15 >= 10)? Resultado: {X >= Y}")

# Menor ou igual a (<=)
print(f"X <= Y (15 <= 10)? Resultado: {X <= Y}")