a = 20
b = 7

print(f"Numeros: a = {a}, b = {b}")

# Adição
resultado_soma = a + b
print(f"Adicao (a + b): {resultado_soma}")

# Subtração
resultado_subtracao = a - b
print(f"Subtracao (a - b): {resultado_subtracao}")

# Multiplicação
resultado_multiplicacao = a * b
print(f"Multiplicacao (a * b): {resultado_multiplicacao}")

# Divisão (Divisão de ponto flutuante - o resultado é 2.857...)
resultado_divisao_float = a / b
print(f"Divisao Float (a / b): {resultado_divisao_float}")

# Divisão Inteira (descarta a parte decimal)
resultado_divisao_int = a // b
print(f"Divisao Inteira (a // b): {resultado_divisao_int}")

# Módulo (Resto da divisão)
resultado_modulo = a % b
print(f"Modulo (a % b): {resultado_modulo}")